package uniandes.insightd.util;

public class Registro {

	private String cause, repayment, causeCategory, repaymentCategory, criterion;
	private String wasPaid;
	private int total, idRow;
	
	public int getIdRow() {
		return idRow;
	}
	public void setIdRow(int idRow) {
		this.idRow = idRow;
	}
	public String getCause() {
		return cause;
	}
	public void setCause(String cause) {
		this.cause = cause;
	}
	public String getRepayment() {
		return repayment;
	}
	public void setRepayment(String repayment) {
		this.repayment = repayment;
	}
	public String getCauseCategory() {
		return causeCategory;
	}
	public void setCauseCategory(String causeCategory) {
		this.causeCategory = causeCategory;
	}
	public String getRepaymentCategory() {
		return repaymentCategory;
	}
	public void setRepaymentCategory(String repaymentCategory) {
		this.repaymentCategory = repaymentCategory;
	}
	public String getCriterion() {
		return criterion;
	}
	public void setCriterion(String criterion) {
		this.criterion = criterion;
	}
	public String getWasPaid() {
		return wasPaid;
	}
	public void setWasPaid(String wasPaid) {
		this.wasPaid = wasPaid;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
	
	
	
}
